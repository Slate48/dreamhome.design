#!/usr/bin/env node
'use strict'
/**
 * seed-from-export.cjs — generate an IDEMPOTENT D1 seed SQL file for the PUBLIC
 * content entities directly from ../../database_export.json (base44's committed
 * export). Uses INSERT OR REPLACE keyed on the base44 record id, so re-running
 * against an updated export is safe and converges D1 to the export.
 *
 *   node workers/api/seed-from-export.cjs            # writes migrations/seed.generated.sql
 * then apply:
 *   node workers/api/cf-wrangler.cjs d1 execute wl-dreamhome-db --remote --file=migrations/seed.generated.sql
 *
 * Only the 7 read entities are seeded (ContactInquiry is a write target, not seeded).
 * base44 export has 0 rows for all portal/transactional entities, so nothing else.
 */
const fs = require('fs')
const path = require('path')

const EXPORT = path.join(__dirname, '../../database_export.json')
const OUT = path.join(__dirname, 'migrations/seed.generated.sql')

// entity -> ordered column list matching 0001_public_content.sql. `_json` marks
// columns stored as a JSON string; `_bool` marks 0/1 integer columns.
const SPEC = {
  PortfolioItem: ['id', 'title', 'category', 'description', 'image_url', ['featured', 'bool'], 'sort_order', 'created_date', 'updated_date'],
  TeamMember:    ['id', 'name', 'title', ['show_title', 'bool'], 'department', 'photo_url', 'bio', ['is_founder', 'bool'], 'sort_order', 'created_date', 'updated_date'],
  FAQItem:       ['id', 'question', 'answer', 'sort_order', ['is_active', 'bool'], 'created_date', 'updated_date'],
  ProcessStage:  ['id', 'stage_number', 'title', 'description', ['is_active', 'bool'], 'created_date', 'updated_date'],
  InvestmentTier:['id', 'step_number', 'title', 'description', 'note', ['payment_methods', 'json'], 'scope_exclusions_note', 'created_date', 'updated_date'],
  Testimonial:   ['id', 'client_name', 'quote', 'rating', 'project_type', ['featured', 'bool'], 'created_date', 'updated_date'],
  SiteSettings:  ['id', 'key', 'phone', 'phone_display', 'email_sales', 'email_billing', 'billing_contact_name', 'address', 'city_state', 'google_maps_embed_url', 'instagram_url', 'facebook_url', 'instagram_handle', 'website_url', 'consultation_booking_url', 'logo_url', 'tagline', 'created_date', 'updated_date'],
}

function sqlVal(v, kind) {
  if (kind === 'json') {
    if (v == null) return 'NULL'
    return `'${JSON.stringify(v).replace(/'/g, "''")}'`
  }
  if (kind === 'bool') return v ? '1' : '0'
  if (v == null) return 'NULL'
  if (typeof v === 'number') return String(v)
  if (typeof v === 'boolean') return v ? '1' : '0'
  return `'${String(v).replace(/'/g, "''")}'`
}

function main() {
  const data = JSON.parse(fs.readFileSync(EXPORT, 'utf8'))
  const entities = data.entities || {}
  const lines = ['-- GENERATED by seed-from-export.cjs — do not edit by hand.',
    `-- source export exported_at: ${data.exported_at}`, 'PRAGMA foreign_keys=OFF;', '']
  const counts = {}
  for (const [entity, spec] of Object.entries(SPEC)) {
    const rows = entities[entity] || []
    counts[entity] = rows.length
    if (!rows.length) continue
    const cols = spec.map(c => Array.isArray(c) ? c[0] : c)
    lines.push(`-- ${entity} (${rows.length})`)
    for (const row of rows) {
      const vals = spec.map(c => {
        const [name, kind] = Array.isArray(c) ? c : [c, null]
        return sqlVal(row[name], kind)
      })
      lines.push(`INSERT OR REPLACE INTO ${entity} (${cols.join(',')}) VALUES (${vals.join(',')});`)
    }
    lines.push('')
  }
  fs.writeFileSync(OUT, lines.join('\n'))
  console.log('wrote', path.relative(path.join(__dirname, '../..'), OUT))
  console.log('row counts:', JSON.stringify(counts))
}
main()
